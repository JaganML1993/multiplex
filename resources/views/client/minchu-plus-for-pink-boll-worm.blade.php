                <h3  class="mb-10">What is Pink Bollworm?</h3>
                <p class="mb-15">Pink bollworm (Pectinophora gossypiella) is a pest found in cotton cultivation. It is a small underground insect. Pink caterpillar is an insect that is active at night and becomes very active in humid environment. The special thing is that this insect persists till the last stage of the crop.</p>
                <p class="mb-15">Female caterpillars lay eggs on cotton bolls and when the larvae hatch from the eggs, they start causing damage to the bolls by eating them. They chew cotton fiber and consume seeds as their food.</p>
                
                <h3  class="mb-10">How to identify attack of Pink Boll Worm?</h3>
                <p class="mb-15">The adult moth is a small, slender, gray moth with mottled wings. The larva is a dull white caterpillar with eight pairs of legs, with distinct pink stripes on its torso. The larvae can be up to half an inch long.</p>
